    private void cargarReparaciones() {
        Connection con = null;
        PreparedStatement ps = null;
        ResultSet rs = null;

        try {
            System.out.println("Iniciando carga de reparaciones...");
            con = Conexion.conectar();

            if (con == null) {
                System.out.println("Error: Conexi贸n es null");
                JOptionPane.showMessageDialog(this, "Error: No se pudo conectar a la base de datos");
                return;
            }

            System.out.println("Conexi贸n establecida correctamente");

            // Obtener el modelo de la tabla
            DefaultTableModel model = (DefaultTableModel) jTableReparaciones.getModel();

            // Limpiar filas existentes
            model.setRowCount(0);
            System.out.println("Tabla limpiada, columnas: " + model.getColumnCount());

            // Query para obtener reparaciones con datos del cliente
            String sql = "SELECT r.idReparacion, r.fechaReparacion, r.precioReparacion, r.descripcion, "
                    + "CONCAT(c.nombre, ' ', c.apellido) AS nombreCompleto "
                    + "FROM tb_reparacion r "
                    + "INNER JOIN tb_cliente c ON r.idCliente = c.idCliente "
                    + "ORDER BY r.idReparacion DESC";

            System.out.println("Ejecutando consulta: " + sql);
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();

            int contador = 0;
            // Llenar la tabla with los datos
            while (rs.next()) {
                Object[] fila = new Object[5];
                fila[0] = rs.getInt("idReparacion");
                fila[1] = rs.getDate("fechaReparacion");
                fila[2] = rs.getString("nombreCompleto");
                fila[3] = String.format("$%.2f", rs.getDouble("precioReparacion"));
                fila[4] = rs.getString("descripcion");

                model.addRow(fila);
                contador++;
                System.out.println("Fila agregada: " + contador + " - ID: " + fila[0]);
            }

            System.out.println("Total de reparaciones cargadas: " + contador);

            // Mensaje informativo si no hay datos
            if (model.getRowCount() == 0) {
                System.out.println("No se encontraron reparaciones");
                JOptionPane.showMessageDialog(this, "No se encontraron reparaciones registradas.\n"
                        + "Verifica que existan registros en las tablas tb_reparacion y tb_cliente.");
            } else {
                System.out.println("Reparaciones cargadas exitosamente en la tabla");
            }

        } catch (SQLException e) {
            System.out.println("Error SQL: " + e.getMessage());
            System.out.println("C贸digo de error: " + e.getErrorCode());
            e.printStackTrace();
            JOptionPane.showMessageDialog(this,
                    "Error al cargar las reparaciones:\n" + e.getMessage()
                    + "\nC贸digo: " + e.getErrorCode(),
                    "Error de Base de Datos",
                    JOptionPane.ERROR_MESSAGE);
        } finally {
            // Cerrar recursos
            try {
                if (rs != null) {
                    rs.close();
                }
                if (ps != null) {
                    ps.close();
                }
                if (con != null) {
                    con.close();
                }
                System.out.println("Recursos cerrados correctamente");
            } catch (SQLException e) {
                System.out.println("Error al cerrar recursos: " + e.getMessage());
            }
        }
    }

